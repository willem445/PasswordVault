namespace PasswordVault.Models
{
    /*=================================================================================================
	ENUMERATIONS
	*================================================================================================*/

    /*=================================================================================================
	STRUCTS
	*================================================================================================*/

    /*=================================================================================================
	CLASSES
	*================================================================================================*/
    public class DatabasePassword : Password
    {
        /*=================================================================================================
		CONSTANTS
		*================================================================================================*/
        /*PUBLIC******************************************************************************************/

        /*PRIVATE*****************************************************************************************/

        /*=================================================================================================
		FIELDS
		*================================================================================================*/
        /*PUBLIC******************************************************************************************/

        /*PRIVATE*****************************************************************************************/

        /*=================================================================================================
		PROPERTIES
		*================================================================================================*/
        /*PUBLIC******************************************************************************************/
        [Browsable(false)]
        public string UserGUID { get; set; }

        /*PRIVATE*****************************************************************************************/

        /*=================================================================================================
		CONSTRUCTORS
		*================================================================================================*/
        public DatabasePassword()
        {

        }

        public DatabasePassword(Int64 uniqueID, string useruuid, string application, string username, string email, string description, string website, string passphrase)
        {
            UniqueID = uniqueID;
            UserGUID = useruuid;
            Application = application;
            Username = username;
            Email = email;
            Description = description;
            Website = website;
            Passphrase = passphrase;
        }

        public DatabasePassword(string userID, string application, string username, string email, string description, string website, string passphrase)
        {
            UserGUID = userID;
            Passphrase = passphrase;
            Application = application;
            Username = username;
            Email = email;
            Description = description;
            Website = website;
        }

        /*=================================================================================================
		PUBLIC METHODS
		*================================================================================================*/
        /*************************************************************************************************/
        public override string GetPasswordString()
        {
            return string.Format(CultureInfo.CurrentCulture, "{0},{1},{2},{3},{4},{5},{6},{7}", UniqueID, UserGUID, Application, Username, Email, Description, Website, Passphrase);
        }

        /*=================================================================================================
		PRIVATE METHODS
		*================================================================================================*/
        /*************************************************************************************************/

        /*=================================================================================================
		STATIC METHODS
		*================================================================================================*/
        /*************************************************************************************************/

    } // DatabasePassword CLASS
} // PasswordVault.Models.Standard NAMESPACE
