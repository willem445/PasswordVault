namespace PasswordVaultTests
{
    /*=================================================================================================
	ENUMERATIONS
	*================================================================================================*/
    public enum Database
    {
        InMemory,
        CSV, 
        MySQL,
        SQLLite,
    }

    /*=================================================================================================
	STRUCTS
	*================================================================================================*/

    /*=================================================================================================
	CLASSES
	*================================================================================================*/
    public static class DatabaseFactory
    {
        /*=================================================================================================
		CONSTANTS
		*================================================================================================*/
        /*PUBLIC******************************************************************************************/

        /*PRIVATE*****************************************************************************************/

        /*=================================================================================================
		FIELDS
		*================================================================================================*/
        /*PUBLIC******************************************************************************************/

        /*PRIVATE*****************************************************************************************/

        /*=================================================================================================
		PROPERTIES
		*================================================================================================*/
        /*PUBLIC******************************************************************************************/

        /*PRIVATE*****************************************************************************************/

        /*=================================================================================================
		CONSTRUCTORS
		*================================================================================================*/

        /*=================================================================================================
		PUBLIC METHODS
		*================================================================================================*/
        /*************************************************************************************************/
        public static IDatabase GetDatabase(Database database)
        {
            IDatabase result = null;

            switch (database)
            {
                case Database.InMemory:
                    result = new InMemoryDatabase();
                    break;

                case Database.CSV:
                    result = new CsvDatabase(
                        new CSVUserManager(
                            new CSVReader(),
                            new CSVWriter()
                            ),
                        new CSVPasswordManager(
                            new CSVReader(),
                            new CSVWriter()
                            )
                        );
                    break;

                case Database.MySQL:
                    result = new MySQLDatabase();
                    break;

                case Database.SQLLite:
                    result = new SQLiteDatabase();
                    break;
            }

            return result;
        }
        /*=================================================================================================
		PRIVATE METHODS
		*================================================================================================*/
        /*************************************************************************************************/

        /*=================================================================================================
		STATIC METHODS
		*================================================================================================*/
        /*************************************************************************************************/

    } // DatabaseFactory CLASS
} // PasswordVaultTests NAMESPACE
